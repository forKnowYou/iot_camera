void TextArea::flush(FunFillRect fun)
{
  _funFillRect = fun;
  flushGraphic(fun);

  uint16_t oneLineMaxCharCount = _w / (_vInterval + FONT_WIDTH) - 1;
  uint16_t widgetLineCount = _h / (_hInterval + FONT_HEIGHT) - 1;
  uint8_t *ts = _textBuffer;
  uint16_t lineCount = 0, oneLineCount = 0;
  for(uint16_t i = 0; i < _textBufferUsed; i ++) {
    if(_textBuffer[i] == '\n') {
      lineCount ++;
      oneLineCount = 0;
    } else if(_textBuffer[i] >= 0x20) {
      oneLineCount ++;
      if(oneLineCount > oneLineMaxCharCount) {
        lineCount ++;
        oneLineCount = 0;
      }
    }
    if(lineCount > widgetLineCount) {
      lineCount --;
      uint16_t tempc = 0;
      while(*ts) {
        if(*ts == '\n') {
          ts ++;
          break;
        } else if(*ts >= 0x20) {
          tempc ++;
          if(tempc > oneLineMaxCharCount) {
            ts ++;
            break;
          }
        }
        ts ++;
      }
    }
  }
  if(ts != _textBuffer) {
    _textBufferUsed -= ts - _textBuffer;
    for(uint16_t i = 0; *ts != 0; i ++, ts ++) {
      _textBuffer[i] = *ts;
      *ts = 0;
    }
  }
  ts = _textBuffer;
  _cursorX = _vInterval;
  _cursorY = _hInterval;
  while(*ts) {
    if(*ts == '\n') {
      _cursorX = 0;
      _cursorY += FONT_HEIGHT + _hInterval;
    } else if(*ts >= 0x20) {
      if((_cursorX + FONT_WIDTH) > _w) {
        _cursorX = 0;
        _cursorY += FONT_HEIGHT + _hInterval;
      }
      ShowOneChar(fun, _x + _cursorX, _y + _cursorY, *ts, _textColor);
      _cursorX += _vInterval + FONT_WIDTH;
    }
    ts ++;
  }
}

void TextArea::show()
{
  flush(_funFillRect);
}

void TextArea::printOne(uint8_t ch)
{
  if(ch == '\b') {
    if(_textBufferUsed) {
      _textBuffer[_textBufferUsed] = 0;
      _textBufferUsed --;
    }
  } else {
    _textBuffer[_textBufferUsed] = ch;
    _textBufferUsed ++;
    _textBuffer[_textBufferUsed] = 0;
  }
}

