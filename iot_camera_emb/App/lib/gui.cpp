void Page::fresh(FunFillRect fun)
{
  uint16_t currentLevel = 0, oldLevel = 0;
  bool done = false;
  while(!done) {
    for(uint16_t i = 0; i < _widgetsLen; i ++) {
      if(_widgets[i]->_visible && (_widgets[i]->_levelIndex == currentLevel)) {
        _widgets[i]->flush(fun);
      }
    }
    done = true;
    for(uint16_t i = 0; i < _widgetsLen; i ++) {
      if((_widgets[i]->_levelIndex > currentLevel) && _widgets[i]->_visible) {
        done = false;
        oldLevel = currentLevel;
        currentLevel = _widgets[i]->_levelIndex;
        for(; i < _widgetsLen; i ++) {
          if(_widgets[i]->_visible && (_widgets[i]->_levelIndex < currentLevel) && (_widgets[i]->_levelIndex > oldLevel))
            currentLevel = _widgets[i]->_levelIndex;
        }
        break;
      }
    }
  }
}

Widget *Page::getWidgetByName(const char * name)
{
  for(uint16_t i = 0; i < _widgetsLen; i ++) {
    if(strcmp((const char *)name, (const char *)_widgets[i]->_name) == 0)
      return _widgets[i];
  }
  return 0;
}

