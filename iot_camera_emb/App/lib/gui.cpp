class CircleHelper {
public:
  struct Point {
    int16_t x, y;
  };

  CircleHelper();

  Point *init(int16_t x, int16_t y, uint16_t r);
  Point *continu();

private:
  int16_t _x, _y, _r;
  int16_t _xt, _yt, _d;
  Point _point;
};

CircleHelper circleHelper;

CircleHelper::CircleHelper()
{

}

CircleHelper::Point *CircleHelper::init(int16_t x, int16_t y, uint16_t r)
{
  _xt = 0;
  _yt = r;
  _d = 3 - 2 * r;
  return continu();
}

CircleHelper::Point *CircleHelper::continu()
{
  if(_xt <= _yt) {
    _point.x = _xt;
    _point.y = _yt;
    if(_d < 0)
      _d += 4 * _xt + 6;
    else {
      _d += 4 * (_xt - _yt) + 10;
      _yt --;
    }
    _xt ++;
    return &_point;
  } else
    return 0;
}

void ShowOneChar(FunFillRect fun, uint16_t x, uint16_t y, uint8_t ch, color_t color)
{
  if(ch < 0x20)
    return;
  uint8_t *p = (uint8_t*) _font[ch - 0x20];
  for(uint8_t i = 0; i < FONT_HEIGHT; i ++) {
    for(uint8_t j = 0; j < FONT_WIDTH; j ++) {
      if(p[i] & (0x80 >> j))
        fun(x + j, y + i, 1, 1, color);
    }
  }
}

void RectHelper(FunFillRect fun, uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t borderWidth, color_t color)
{
  fun(x, y - borderWidth / 2, w, borderWidth, color);
  fun(x - borderWidth / 2, y, borderWidth, h, color);
  fun(x, y - borderWidth / 2 + h, w, borderWidth, color);
  fun(x - borderWidth / 2 + w, y, borderWidth, h, color);
}

